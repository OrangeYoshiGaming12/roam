function loopTile (delta: number) {
    dist = 27
    scale = 16
    tile = tiles.getTileLocation((Render.getRenderSpriteInstance().x + Render.getAttribute(Render.attribute.dirX) * dist) / scale, (Render.getRenderSpriteInstance().y + Render.getAttribute(Render.attribute.dirY) * dist) / scale)
    if (lastTile.x == tile.x && lastTile.y == tile.y) {
        indexTexture += delta
    } else if (indexTexture == list.indexOf(tiles.tileImageAtLocation(tile))) {
        indexTexture += delta
    }
    indexTexture = (indexTexture + list.length) % list.length
    tiles.setTileAt(tile, list[indexTexture])
    info.setScore(indexTexture)
    if (indexTexture == 0) {
        tiles.setWallAt(tile, false)
    } else {
        tiles.setWallAt(tile, true)
    }
    lastTile = tile
}
